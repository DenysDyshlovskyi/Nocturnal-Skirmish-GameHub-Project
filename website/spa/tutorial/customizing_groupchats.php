    <h1>Customizing groupchats</h1>
    <p>
        To change the groupchat name and picture, you can click the <b>cogwheel</b> next to the groupchat name.
        A menu will pop up showing the members of the groupchat as well as some buttons.
        To change the groupchat name, simply click the groupchat name, edit it, and either press <b>Enter</b> or click <b>"Save changes"</b>.
        To change the groupchat image, click on the groupchat image and a menu will pop up. Upload your .png or .jpg file, crop it and save it.
    </p>
