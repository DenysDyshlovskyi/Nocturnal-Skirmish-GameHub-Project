    <h1>Creating chats</h1>
    <p>
        Now that you've added some friends it might be time to chat with them.
        To create a private chat with your friend, go to the <b>Friends list</b> and click on the <b>chat bubble icon</b>.
    </p>
    <br>
    <img src="./img/tutorial/create_chats_through_friendlist.gif">
    <br>
    <p>
        There will be a notification visible from the main hub page, when you receive a new chat.
        You can also create a chat by clicking on the <b>chat bubble icon</b> on the main hub, clicking the <b>+</b> icon, selecting <b>"Create private message"</b>,
        and clicking <b>"Send message"</b> next to your friends name.
    </p>
