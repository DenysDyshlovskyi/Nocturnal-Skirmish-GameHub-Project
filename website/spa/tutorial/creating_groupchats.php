    <h1>Creating groupchats</h1>
    <p>
        You can also create groupchats on GameHub. To do this, head to the messages page, click the <b>+</b> icon and click <b>"Create groupchat"</b>.
        A menu of your friends will pop up. You can only add your friends to the groupchat and you need to add atleast 2 people.
        Click the checkbox next to the friends you want to add and click <b>"Create groupchat"</b>
